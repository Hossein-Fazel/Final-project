std::string Organisation::get_biography() const
{
    return organisation_bio;
}

//------------------------------------------------------------------------

void Organisation::set_biography(std::string bio)
{
    if (bio.length() > 1100)
        organisation_bio = bio.erase(160, bio.length());

    else   
        organisation_bio = bio;
}


